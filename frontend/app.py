import streamlit as st
import time
import requests

# The backend URL is never hardcoded — set BACKEND_URL in Streamlit secrets.
# st.secrets raises if no secrets file exists at all, so a missing config has to
# surface as a message rather than a crash on import.
try:
    BACKEND_URL = st.secrets.get("BACKEND_URL", "").rstrip("/")
except Exception:
    BACKEND_URL = ""

# Render's free tier spins down after 15 minutes idle and can take ~60s to wake.
# Every call gets a generous timeout so a cold start reads as "waking up" rather
# than as a crash.
TIMEOUT = 180
COLD_START_HINT = (
    "The backend didn't respond in time. On Render's free tier it spins down after "
    "15 minutes idle and takes up to a minute to wake up. Give it a moment and try again."
)


class BackendError(Exception):
    pass


def call_backend(method: str, path: str, **kwargs):
    """Single funnel for every backend call, so no request can crash the app raw."""
    if not BACKEND_URL:
        raise BackendError(
            "BACKEND_URL is not configured. Add it to this app's Streamlit secrets."
        )

    try:
        response = requests.request(
            method, f"{BACKEND_URL}{path}", timeout=TIMEOUT, **kwargs
        )
    except requests.exceptions.Timeout:
        raise BackendError(COLD_START_HINT)
    except requests.exceptions.ConnectionError:
        raise BackendError(
            f"Could not reach the backend at {BACKEND_URL}. {COLD_START_HINT}"
        )
    except requests.exceptions.RequestException as e:
        raise BackendError(f"Request failed: {e}")

    if not response.ok:
        try:
            detail = response.json().get("detail", response.text)
        except ValueError:
            detail = response.text
        raise BackendError(f"Backend returned {response.status_code}: {detail}")

    return response.json()

