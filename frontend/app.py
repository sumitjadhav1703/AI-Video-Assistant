# ── Run Pipeline ────────────────────────────────────────────────────────────────
STEP_ORDER = ["audio", "transcript", "title", "summary", "extract", "rag"]
STEP_LABELS = [
    ("audio",      "🔊", "Audio Processing"),
    ("transcript", "📝", "Transcription"),
    ("title",      "🏷️", "Title Generation"),
    ("summary",    "📋", "Summarisation"),
    ("extract",    "🔍", "Extraction"),
    ("rag",        "🧠", "RAG Engine"),
]


def steps_from(current: str) -> dict:
    """Everything before the backend's current step is done; the current one is active."""
    if current not in STEP_ORDER:
        return {}
    idx = STEP_ORDER.index(current)
    return {
        s: ("done" if i < idx else "active" if i == idx else "pending")
        for i, s in enumerate(STEP_ORDER)
    }


if run_btn:
    if input_mode == "YouTube URL" and not (youtube_url or "").strip():
        st.error("Please enter a YouTube URL.")
    elif input_mode == "Upload file" and upload is None:
        st.error("Please upload an audio or video file.")
    else:
        st.session_state.pipeline_done = False
        st.session_state.result = None
        st.session_state.chat_history = []
        st.session_state.pipeline_steps = {}
        st.session_state.job_id = None

        progress_placeholder = st.empty()

        try:
            # Start the job. The backend returns a job_id immediately and does the
            # multi-minute work in the background — nothing is processed inline.
            if input_mode == "YouTube URL":
                started = call_backend(
                    "POST", "/jobs",
                    data={"youtube_url": youtube_url.strip(), "language": language},
                )
            else:
                started = call_backend(
                    "POST", "/jobs",
                    data={"language": language},
                    files={"file": (upload.name, upload.getvalue())},
                )

            job_id = started["job_id"]
            st.session_state.job_id = job_id

            # Poll until the backend says done or error.
            while True:
                status = call_backend("GET", f"/jobs/{job_id}")

                if status["status"] == "done":
                    break
                if status["status"] == "error":
                    raise BackendError(status.get("error") or "Processing failed.")

                st.session_state.pipeline_steps = steps_from(status.get("step") or "")
                with progress_placeholder.container():
                    st.info("⚙️ Pipeline running — this can take a few minutes…")
                    for step, icon, label in STEP_LABELS:
                        render_step_bar(label, step, icon)

                time.sleep(3)

            st.session_state.pipeline_steps = {s: "done" for s in STEP_ORDER}
            st.session_state.result = call_backend("GET", f"/jobs/{job_id}/result")
            st.session_state.pipeline_done = True
            progress_placeholder.success("✅ Analysis complete!")
            time.sleep(0.5)
            progress_placeholder.empty()
            st.rerun()

        except BackendError as e:
            for k in STEP_ORDER:
                if st.session_state.pipeline_steps.get(k) == "active":
                    st.session_state.pipeline_steps[k] = "pending"
            progress_placeholder.error(f"❌ {e}")

